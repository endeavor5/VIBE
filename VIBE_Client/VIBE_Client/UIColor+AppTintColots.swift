import UIKit

extension UIColor {
  class func appBluishGreenTinColor() -> UIColor {
    return UIColor(red: 20/255.0, green: 160/255.0, blue: 160/255.0, alpha: 1.0)
  }
  
  class func appWindowTinColor() -> UIColor {
    return UIColor(red: 10/255.0, green: 80/255.0, blue: 80/255.0, alpha: 1.0)
  }
  
}

